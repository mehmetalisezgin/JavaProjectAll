package _05_ForLoop;

public class Q3_ok {

	public static void main(String[] args) {
		// 0-255 e kadar olan harflerin , sayı ve harf değerini konsola yazdırınız.
       
		int count  = 0 ;
		int a = 256 ;
		for (int i = 0; i < a; i++) {
			char harf  = (char) i ;
			count ++ ;
			System.out.println(count);
			System.out.println(harf);
		}
		
		 
			
			
			
			
	
	}
}
			
			
		
		
		
		
		
		
		
	


